from django.shortcuts import render, redirect
from django.contrib import messages
from django.contrib.auth.decorators import login_required
from .forms import UserRegisterForm, UserUpdateForm, ProfileUpdateForm
from .decorators import unauthenticated_user
from exchange.models import Student

@unauthenticated_user
def register(request):

    if request.method == 'POST':
        form = UserRegisterForm(request.POST)
        if form.is_valid():

            user = form.save()
            user.save()
            
            student, created = Student.objects.get_or_create(index_number = form.cleaned_data.get('index'))

            if created:
                student.index_number = form.cleaned_data.get('index')
                
            student.semester = form.cleaned_data.get('semester')  
            student.user = user
            student.save()

            username = form.cleaned_data.get('username')

            messages.success(request, f'Account created for {username}. You can now log in.')
            return redirect('login')
    else:
        form = UserRegisterForm()
    
    return render(request, 'users/register.html', {'form': form})


@login_required
def profile(request):
    return render(request, 'users/profile.html')


@login_required
def update_profile(request):

    if request.method == 'POST':
        u_form = UserUpdateForm(request.POST, instance=request.user)
        p_form = ProfileUpdateForm(request.POST, instance=request.user.student)

        if u_form.is_valid() and p_form.is_valid():
            u_form.save()
            p_form.save()

            messages.success(request, f'Your account has been updated')
            return redirect('profile')

    else:
        u_form = UserUpdateForm(instance=request.user)
        p_form = ProfileUpdateForm(instance=request.user.student)

    context = {
        'u_form' : u_form,
        'p_form' : p_form
    }

    return render(request, 'users/update_profile.html', context)


@login_required
def delete_profile(request):

    user = request.user

    if request.method == 'POST':
        user.delete()
        return redirect('/')        

    return render(request, 'users/user_confirm_delete.html')